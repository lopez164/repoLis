    private void grabar() throws HeadlessException {
        int x = JOptionPane.showConfirmDialog(this, "Desea guardar el registro ?", "Guardar registro", JOptionPane.OK_CANCEL_OPTION);
        if (x == JOptionPane.OK_OPTION) {
            recogida = new CRecogidasPorFactura(ini);
            recogida.setActivo(1);
            recogida.setFlag(1);
            recogida.setNumeroDocumento(txtNumeroFormato.getText().trim());
            recogida.setNumeroFactura(txtNumeroFactura.getText().trim());
            recogida.setValorRecogida(Double.parseDouble(txtValorDescuento.getText().trim()));

            txtNumeroFormato.setEditable(false);
            txtValorDescuento.setEditable(false);
            
            if (validar()) {

                  new Thread(new HiloGuardarRecogidaPorFactura(this)).start();
                actualizar = false;
            } else {
                this.btnGrabar.setIcon(new javax.swing.ImageIcon(getClass().getResource("/aplicacionlogistica/configuracion/imagenes/grabar.png"))); // NOI18N
                JOptionPane.showInternalMessageDialog(this, mensaje, "Error al guardar", 0);
            }
        }
    }

