 public boolean validar() {
        boolean verificado = true;
        mensaje = "";
        if (txtNumeroManifiesto.getText().isEmpty()) {
            mensaje += "No ha colocado el nombre del Documento" + "  \n";
            verificado = false;
        }
    
    return verificado;
    
}
private boolean guardarRegistroDocumento(){
     boolean valida=false;
    try {
       
          CDocumentos documento = new CDocumentos(ini);  
          documento.setNombreTipoDocumento(txtNumeroManifiesto.getText().trim());
          documento.setFormato(txtNumeroSoporte.getText());
//          if(chkActivo.isSelected()){
//           documento.setActivoDocumento(1);
//          }else{
//               documento.setActivoDocumento(1);
//          }
          valida=documento.grabarTiposDocumentos();
          if(valida){
             
//             jLabel2.setVisible(true);
//             jLabel2.setText("Id. Documento :  " + documento.getIdtiposDocumentos());  
              fila = new Object[4];
                    fila[0] = documento.getIdtiposDocumentos();
                    fila[1] = documento.getNombreTipoDocumento();
                    fila[2] = documento.getFormato();
                    if(documento.getActivoDocumento()<1){
                       fila[3] = false;
                    }else{
                       fila[3] = true; 
                    }
                    modelo = (DefaultTableModel) jTable1.getModel();
                    modelo.addRow(fila);
          }
         
          
    } catch (Exception ex) {
        Logger.getLogger(FIngresarConsignacionesRutas.class.getName()).log(Level.SEVERE, null, ex);
    }
    return valida;
}

private boolean actualizarRegistroCargo(){
     boolean valida=false;
    try {
         int row=jTable1.getSelectedRow();
         //jLabel2.setText("Id. Documento :  " + String.valueOf(jTable1.getValueAt(row, 0)));
          CDocumentos documento = new CDocumentos(ini); 
          documento.setIdtiposDocumentos((Integer)jTable1.getValueAt(row, 0));
          documento.setNombreTipoDocumento(txtNumeroManifiesto.getText().trim());
          documento.setFormato(txtNumeroSoporte.getText().trim());
//          if(chkActivo.isSelected()){
//           documento.setActivoDocumento(1);
//          }else{
//               documento.setActivoDocumento(0);
//          }
         
          valida=documento.actualizarDocumentos(1);
          
           if(valida){
             jTable1.setValueAt(documento.getNombreTipoDocumento(), row, 1);
            // jLabel2.setVisible(true);
            // jLabel2.setText("Id. Documento :  " + documento.getIdtiposDocumentos());  
          }
    } catch (Exception ex) {
        Logger.getLogger(FIngresarConsignacionesRutas.class.getName()).log(Level.SEVERE, null, ex);
    }return valida;
}

