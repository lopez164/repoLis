    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        if (!txtNumeroManifiesto.getText().isEmpty()) {
            int x = JOptionPane.showConfirmDialog(this, "Desea guardar el registro ?", "Guardar registro", 0);
            if (x == 0) {
             if(actualizar){
                  if (validar()) {
                    if (actualizarRegistroCargo()) {
                       // new Thread(new HiloGuardarEmpleado(5,empleadoNuevo)).start();
                        JOptionPane.showInternalMessageDialog(this, "El registro del empleado ha sido actualizado perfectamente", "Registro actualizado", 1);
                       
                        habilitar(false);

                    } else {
                        JOptionPane.showInternalMessageDialog(this, "Se presentó un error al guardar el regisstro", "Error al guardar", 0);
                    }

                } else {
                    JOptionPane.showInternalMessageDialog(this, "mensaje", "Error al guardar", 0);
                } 
             }else{
                if (validar()) {
                    if (guardarRegistroDocumento()) {
                       // new Thread(new HiloGuardarEmpleado(5,empleadoNuevo)).start();
                        JOptionPane.showInternalMessageDialog(this, "El registro del empleado ha sido guardado perfectamente", "Registro guardado", 1);
                       
                        habilitar(false);

                    } else {
                        JOptionPane.showInternalMessageDialog(this, "Se presentó un error al guardar el regisstro", "Error al guardar", 0);
                    }

                } else {
                    JOptionPane.showInternalMessageDialog(this, "mensaje", "Error al guardar", 0);
                } 
             }
                
            
            }
        } else {
            JOptionPane.showInternalMessageDialog(this, "No se pueden Guardar Datos con campos vacíos", "Error al guardar", 2);
        }
    }//GEN-LAST:event_btnGrabarActionPerformed

